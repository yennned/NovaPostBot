"""Кнопка нижней панели — это навигация, а не ввод.

Хендлеры, ждущие произвольный текст в своём FSM-состоянии (поиск товаров и
отправлений, ввод телефона сотрудника, дата в отчётах, релей поддержки),
матчатся ДО хендлера самой кнопки — по порядку роутеров или по порядку
регистрации внутри роутера. Из-за этого тап «⚙️ Налаштування» в таком состоянии
уходил в них как «ввод»: клиент получал «Вкажіть коректний номер телефону» или
пустой результат поиска вместо экрана настроек — и кнопка выглядела сломанной.

Лечение — из двух частей, обе обязательны:

1. Этот роутер (подключён ПЕРВЫМ) снимает FSM-стейт на тапе любой кнопки меню,
   чтобы следующий свободный текст не ушёл в брошенный сценарий.
2. Каждый хендлер, ловящий произвольный текст в стейте, обязан исключать
   `MENU_TEXTS` (`~F.text.in_(MENU_TEXTS)`).

Почему одного этого роутера мало: `SkipHandler` НЕ выбрасывает событие из
роутера — он передаёт его следующему хендлеру того же роутера, а дальше уходит
в следующий роутер. И `state.clear()` здесь не влияет на фильтры ниже: `raw_state`
резолвится middleware один раз на апдейт, поэтому `StateFilter` у хендлеров ниже
всё ещё видит старый стейт. Без пункта 2 кнопку снова съедят.
"""

from __future__ import annotations

from aiogram import F, Router
from aiogram.dispatcher.event.bases import SkipHandler
from aiogram.fsm.context import FSMContext
from aiogram.types import Message

from app.bot.keyboards.menus import MENU_TEXTS

router = Router(name="menu_escape")


@router.message(F.text.in_(MENU_TEXTS))
async def menu_button_resets_state(message: Message, state: FSMContext) -> None:
    """Снять стейт и отдать событие дальше — обработает хендлер самой кнопки."""
    await state.clear()
    raise SkipHandler()
